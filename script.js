const select = document.querySelector(".select");

const texts = document.querySelectorAll(".text");

let lang = "en";

select.addEventListener("change", setLanguage);

function setLanguage() {
  lang = select.value;
  texts.forEach((text) => {
    let data = text.getAttribute("data-text");

    if (lang === "fa") {
      for (const item in languages.fa) {
        if (item === data) {
          text.innerHTML = languages.fa[item];
          text.style.textAlign = "right";
        }
      }
    }
    if (lang === "en") {
      for (const item in languages.en) {
        if (item === data) {
          text.innerHTML = languages.en[item];
          text.style.textAlign = "left";
        }
      }
    }
  });
}
